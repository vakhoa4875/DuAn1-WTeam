package model;

import java.time.LocalDateTime;

/**
 * 
 * @author Vu Anh Khoa <vakhoa4875@gmail.com>
 */
public class KhuyenMai {
    private String maKhuyenMai;
    private LocalDateTime ngayStart, ngayEnd;
    private Integer soLuong, discount, max, minSpent;

    public KhuyenMai() {
    }

    public KhuyenMai(String maKhuyenMai, LocalDateTime ngayStart, LocalDateTime ngayEnd, Integer soLuong, Integer discount, Integer max, Integer minSpent) {
        this.maKhuyenMai = maKhuyenMai;
        this.ngayStart = ngayStart;
        this.ngayEnd = ngayEnd;
        this.soLuong = soLuong;
        this.discount = discount;
        this.max = max;
        this.minSpent = minSpent;
    }

    public String getMaKhuyenMai() {
        return maKhuyenMai;
    }

    public void setMaKhuyenMai(String maKhuyenMai) {
        this.maKhuyenMai = maKhuyenMai;
    }

    public LocalDateTime getNgayStart() {
        return ngayStart;
    }

    public void setNgayStart(LocalDateTime ngayStart) {
        this.ngayStart = ngayStart;
    }

    public LocalDateTime getNgayEnd() {
        return ngayEnd;
    }

    public void setNgayEnd(LocalDateTime ngayEnd) {
        this.ngayEnd = ngayEnd;
    }

    public Integer getSoLuong() {
        return soLuong;
    }

    public void setSoLuong(Integer soLuong) {
        this.soLuong = soLuong;
    }

    public Integer getDiscount() {
        return discount;
    }

    public void setDiscount(Integer discount) {
        this.discount = discount;
    }

    public Integer getMax() {
        return max;
    }

    public void setMax(Integer max) {
        this.max = max;
    }

    public Integer getMinSpent() {
        return minSpent;
    }

    public void setMinSpent(Integer minSpent) {
        this.minSpent = minSpent;
    }
    
}
